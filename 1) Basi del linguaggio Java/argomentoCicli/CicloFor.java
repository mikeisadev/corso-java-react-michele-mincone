
public class CicloFor {

	public static void main(String[] args) {
		/**
		 * Altri cicli da vedere in JAVA:
		 * 
		 * - for
		 * - for-each (Lo vediamo nella prossima classe java)
		 * 
		 * Il loro utilizzo Ã¨ molto versatile per ottenere dati dagli array per poi
		 * eseguire ulteriori calcoli o istruzioni.
		 * 
		 * Ma possiamo partire da esempi di cicli for molto semplici:
		 */
		
		// 1. CICLO FOR
		for (int i = 0; i <= 10; i++) {
			System.out.println("Ciclo for: " + i);
		}
		
		// 1.1 CICLO FOR INVERTITO
		for (int i = 10; i >= 0; i--) {	
			System.out.println("Ciclo for invertito: " + i);
		}
		

	}

}
